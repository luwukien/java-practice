public class Taxi {

    private String driver;
    private int salary;

    public Taxi() {
    }

    public Taxi(String driver, int salary) {
        this.driver = driver;
        this.salary = salary;
    }

    public String getDriver() {
        return driver;
    }

    public void setDriver(String driver) {
        this.driver = driver;
    }

    public int getSalary() {
        return salary;
    }

    public void setSalary(int salary) {
        this.salary = salary;
    }


}
